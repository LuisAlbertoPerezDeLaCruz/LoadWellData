    public void cargaTabla() {
        ResultSet rs;
        int cant=0;
        LASPerMD oLASPerMD=new LASPerMD();
        DecimalFormat df = new DecimalFormat("##########0.000");
        
        rs=oBD.select("SELECT count(*) FROM LASPerMD WHERE lasId="+lasId);
        if (rs!=null) {
            try {
                while (rs.next()) {
                    cant=rs.getInt(1);
                }
            } catch (SQLException ex) {
                Logger.getLogger(MuestraLASPerMD.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (cant>0) {
            Object[] o = new Object[cant];
            try {
                o=oBD.select(LASPerMD.class, "lasId="+lasId);
            } catch (InstantiationException | IllegalAccessException ex) {
                Logger.getLogger(MuestraLASPerMD.class.getName()).log(Level.SEVERE, null, ex);
            }
            for (int i=0;i<=cant-1;i++) {
                oLASPerMD=(LASPerMD) o[i];
                modelo.addRow(new Object[] { oLASPerMD.getDept(),
                preparaValor(df.format(oLASPerMD.getGr())),
                preparaValor(df.format(oLASPerMD.getP40hunc())),
                preparaValor(df.format(oLASPerMD.getTvde())),
                preparaValor(df.format(oLASPerMD.getCrpm())),
                preparaValor(df.format(oLASPerMD.getAzimCont())),
                preparaValor(df.format(oLASPerMD.getInclCont())) });
            }
        }
    }    
        
    private Object preparaValor(Object o) {
        if (o.toString().contains(""+valorNulo)) {
            return valorNuloMuestra;
        } else
            return o;
    }

